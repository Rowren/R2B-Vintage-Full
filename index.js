function rendercart(){
    if(cart.length > 0){
        var html = '';
        for ( let i=0; i<cart.length; i++){
            html += `<div class="cartlitst-item">
            <div class="cartlitst-left">
                <img src="${cart[i].img}" alt="">
                <div class="cartlitst-detail">
                    <p style="font-size: 1.5vw;">${cart[i].name}</p>
                    <p style="font-size: 1vw;">${ numberWithCommas(cart[i].price * cart[i].count) } THB</p>
                </div>
            </div>
            <div class="cartlitst-right">
            <p onclick="deinitems('-', ${i})" class="btnc">-</p>
                <p id="countitems" style="margin: 0 10px;">${cart[i].count}</p>
                <p onclick="deinitems('+', ${i})" class="btnc">+</p>
            </div>
        </div>`;
        }
        $("#mycart").html(html)
    }
    else{
        $("#mycart").html(`<p>Not found product litst</p>`)
    }
}

function deinitems(action, index) {
    if(action == '-') {
        if(cart[index].count > 0) {
            cart[index].count--;
            $("#countitems"+index).text(cart[index].count)

            if(cart[index].count <= 0) {
                Swal.fire({
                    icon: 'warning',
                    title: 'Are you sure to delete?',
                    showConfirmButton: true,
                    showCancelButton: true,
                    confirmButtonText: 'Delete',
                    cancelButtonText: 'Cancel'
                }).then((res) => {
                  if(res.isConfirmed) {
                     cart.splice(index, 1) 
                     console.log(cart)
                     rendercart();
                     $("#cartcount").css('display','flex').text(cart.length)
                     
                     if(cart.length <= 0) {
                        $("#cartcount").css('display','none')
                     }
                  }  
                  else {
                    cart[index].count++;
                    $("#countitems"+index).text(cart[index].count)
                    rendercart();
                  }
                })
            }
            rendercart();
        }
        
    }
    else if(action == '+') {
        cart[index].count++;
        $("#countitems"+index).text(cart[index].count)
        rendercart();
    }
}

